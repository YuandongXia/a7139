#include "define.h"

/**********************************************************
* Delay10us @12MHz
**********************************************************/
void Delay10us(Uint8 n)
{
    Uint8 i;
	
	while(n--)
	{
        for(i=0; i<12; i++);
	}
}

/**********************************************************
* Delay100us @12MHz
**********************************************************/
void Delay100us(Uint8 n)
{
    Uint8 i;

    while(n--)
	{
        for(i=0; i<120; i++);
	}
}

/**********************************************************
* Delay1ms @12MHz
**********************************************************/
void Delay1ms(Uint8 n)
{
    Uint8 i, j;

    while(n--)
    {
        for(i=0; i<10; i++)
            for(j=0; j<245; j++);
    }
}

/**********************************************************
* Delay10ms @12MHz
**********************************************************/
void Delay10ms(Uint8 n)
{
    Uint8 i, j;

    while(n--)
    {
        for(i=0; i<100; i++)
            for(j=0; j<245; j++);
    }
}

